fun main(){
    //for loops
    for(number in 1..10){
        println(number)
    }

    println("................................")

    for(counter in 20..50){
        println("The counter is $counter")
    }

    println("................................")

    for (i in 10..50 step 2){
        println("The number is $i")
    }

    println("................................")

    for (num in 50..60 step 2){
        println(num)
    }

    println("................................")

    //printing numbers in reverse

    for (number in 10 downTo 1){
        println("The number is $number")
    }   

}